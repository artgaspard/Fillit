#include "basics/chars.h"

void	ft_putnbr(int n)
{
	unsigned int	nb;

	nb = n;
	if (n < 0)
	{
		ft_putchr('-');
		nb = -n;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchr((nb % 10) + '0');
}
