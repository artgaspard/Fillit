void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	char			*ptr;

	if (s && f)
	{
		i = 0;
		ptr = s;
		while (*ptr)
		{
			f(i, ptr);
			i++;
			ptr++;
		}
	}
}
