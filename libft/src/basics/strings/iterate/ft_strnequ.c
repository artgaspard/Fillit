#include <stdlib.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	char	*p1;
	char	*p2;

	if (!s1 || !s2 || !n)
		return (1);
	p1 = (char *)s1;
	p2 = (char *)s2;
	while (*p1 == *p2 && *p1 && *p2 && --n)
	{
		p1++;
		p2++;
	}
	return ((!(*p1) && !(*p2)) || (*p1 == *p2)) ? 1 : 0;
}
