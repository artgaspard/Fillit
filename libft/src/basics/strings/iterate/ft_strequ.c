int		ft_strequ(char const *s1, char const *s2)
{
	char	*p1;
	char	*p2;

	if (!s1 || !s2)
		return (0);
	p1 = (char *)s1 - 1;
	p2 = (char *)s2 - 1;
	while (*(++p1) == *(++p2) && *p1 && *p2)
		;
	return (!(*p1) && !(*p2)) ? 1 : 0;
}
